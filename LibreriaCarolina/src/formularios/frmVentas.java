    public double Totalizar() {
        int contar = tblDetalle.getRowCount();
        double Totalizar = 0;

        for (int i = 0; i < contar; i++) {
            Totalizar = Totalizar + Double.parseDouble(tblDetalle.getValueAt(i, 5).toString());
        }

        return Totalizar;
    }
   
